    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
        if (validarAtualizacao() == false) {
            return;
        }
        int codigoCab = 0;
        int codigoDet = 0;
        int controle = 0;
        if (jTextFieldCodigo.getText().equals((""))) {
            try {
                codigoCab = (m_loc.buscaLocacaoUltimo().getCodigoCab()) + 1;
            } catch (Exception ex) {
                codigoCab = 1;
            }
        } else {
            try {
                codigoCab = Integer.parseInt(jTextFieldCodigo.getText());
                controle = 1;
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage(),
                        "Cadastro de Locações", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        try {
            codigoDet = (m_loc.buscaLocacaoUltimo().getCodigoDet()) + 1;
        } catch (Exception ex) {
            codigoDet = 1;
        }

        try {
            DateFormat formaterLoc = new SimpleDateFormat("dd/MM/yyyy");
            String dtLoc = jFormattedTextFieldDataLoc.getText();
            java.util.Date parsedUtilDateLoc = formaterLoc.parse(dtLoc);
            java.sql.Date DataLoc = new java.sql.Date(parsedUtilDateLoc.getTime());

            java.sql.Date DataDev = null;

            if (!jFormattedTextFieldDataDev.getText().equals("")) {

                if (!LtpUtil.validarData(jFormattedTextFieldDataDev.getText().trim())) {
                    JOptionPane.showMessageDialog(this, "Data de Devolução Inválida.",
                            "Cadastro de Locações", JOptionPane.WARNING_MESSAGE);
                    jFormattedTextFieldDataDev.requestFocus();
                    return;
                }
                DateFormat formaterDev = new SimpleDateFormat("dd/MM/yyyy");
                String dtDev = jFormattedTextFieldDataDev.getText();
                java.util.Date parsedUtilDateDev = formaterDev.parse(dtDev);
                DataDev = new java.sql.Date(parsedUtilDateDev.getTime());
            }

            int codCliente = Integer.parseInt(jFormattedTextFieldCodCliente.getText());
            int codFilme = Integer.parseInt(jFormattedTextFieldCodFilme.getText());

            Double valor = 0.0;

            if (!jFormattedTextFieldValor.getText().equals("")) {
                try {
                    valor = Double.parseDouble(jFormattedTextFieldValor.getText());
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage(),
                            "Cadastro de Locações", JOptionPane.INFORMATION_MESSAGE);
                }
            }

            m_loc.adicionarLocacao(new Locacao(codigoCab, codCliente, DataLoc, DataDev, codigoDet, codFilme, valor));

            if (controle == 0) {
                JOptionPane.showMessageDialog(this, "Locação Cadastrada.",
                        "Cadastro de Locações", JOptionPane.INFORMATION_MESSAGE);
            } else {

                JOptionPane.showMessageDialog(this, "Filme Adicionado.",
                        "Cadastro de Locações", JOptionPane.INFORMATION_MESSAGE);

            }
            limparForm();


        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage(),
                    "Cadastro de Locações", JOptionPane.INFORMATION_MESSAGE);
        }

}//GEN-LAST:event_jButtonIncluirActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        if (jTextFieldCodigo.getText().equals("") || jTextFieldCodigo == null) {
            return;
        }
        if (JOptionPane.showConfirmDialog(null, "Confirmar a exclusão da locação - "
                + jFormattedTextFieldCodFilme.getText().trim() + "?",
                "Cadastro de Locações",
                JOptionPane.YES_NO_OPTION) == JOptionPane.NO_OPTION) {
            return;
        }
        try {
            m_loc.excluirCliente(Integer.parseInt(jTextFieldCodigo.getText()));
            JOptionPane.showMessageDialog(this, "Locação Excluída.",
                    "Cadastro de Locações", JOptionPane.INFORMATION_MESSAGE);
            limparForm();

        } catch (SQLException objErro) {
            JOptionPane.showMessageDialog(this, objErro.getMessage(),
                    "Cadastro de Locações", JOptionPane.WARNING_MESSAGE);

        }
}//GEN-LAST:event_jButtonExcluirActionPerformed

