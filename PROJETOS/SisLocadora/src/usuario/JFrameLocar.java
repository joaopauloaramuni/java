    private void jButtonExecutarRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExecutarRelatorioActionPerformed
GregorianCalendar menorData = new GregorianCalendar();
        GregorianCalendar maiorData = new GregorianCalendar();

        if (!LtpUtil.validarData(jTextFieldParametroinicio.getText(), menorData)) {
            JOptionPane.showMessageDialog(
                    this,
                    "Menor data inválida.",
                    "Relação de Clientes",
                    JOptionPane.WARNING_MESSAGE);
            jTextFieldParametroinicio.requestFocus();
            return;
        }
        if (!LtpUtil.validarData(jTextFieldParametrofim.getText(), maiorData)) {
            JOptionPane.showMessageDialog(
                    this,
                    "Maior data inválida.",
                    "Relação de Clientes",
                    JOptionPane.WARNING_MESSAGE);
            jTextFieldParametrofim.requestFocus();
            return;
        }
        if (menorData.compareTo(maiorData)>0) {
            JOptionPane.showMessageDialog(
                    this,
                    "Menor data maior que a maior data",
                    "Relação de Clientes",
                    JOptionPane.WARNING_MESSAGE);
            jTextFieldParametroinicio.requestFocus();
            return;
        }
        HashMap<String,Object> parametros = new HashMap<String, Object>();
        parametros.put("DATA", menorData.getTime());
        parametros.put("DATA2", maiorData.getTime());

        try {
            LtpUtil.gerarRelatorio(
                    LtpUtil.RELATORIO_VISUALIZADOR_JASPER,
                    bdlocadora.BDLocadora.objCon,
                    parametros,
                    getClass().getResource("relatorios/Relatorio_L.jasper").openStream(),
                    "Relação de Locações de Filmes");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
}//GEN-LAST:event_jButtonExecutarRelatorioActionPerformed

