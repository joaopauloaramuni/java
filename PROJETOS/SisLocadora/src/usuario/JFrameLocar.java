    private void exibirLocacao(Locacao obj) {
        jTextFieldCodigo.setText(String.valueOf(obj.getCodigoCab()));
        jFormattedTextFieldCodCliente.setText(String.valueOf(obj.getCodigoCliente()));
        jFormattedTextFieldDataLoc.setText(LtpUtil.formatarData(obj.getDataLocacao(), "dd/MM/yyyy"));

        if (obj.getDataDevolucao() != null) {
            jFormattedTextFieldDataDev.setText(LtpUtil.formatarData(obj.getDataDevolucao(), "dd/MM/yyyy"));
        }
        jFormattedTextFieldCodFilme.setText(String.valueOf(obj.getCodigoFilme()));
        jFormattedTextFieldValor.setText(obj.getValor().toString());
    }

    private void limparForm() {
        jTextFieldCodigo.setText("");
        jFormattedTextFieldCodCliente.setText("");
        jFormattedTextFieldDataLoc.setText("");
        jFormattedTextFieldDataDev.setText("");
        jFormattedTextFieldCodFilme.setText("");
        jFormattedTextFieldValor.setText("");
    }

    private boolean validarAtualizacao() {

        if (jFormattedTextFieldCodFilme.getText().trim().equals("") || !jFormattedTextFieldCodFilme.getText().matches("[0-9]*")) {
            JOptionPane.showMessageDialog(this, "Código do Filme Inválido.",
                    "Cadastro de Locações", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldCodFilme.requestFocus();
            return false;
        }
        if (jFormattedTextFieldCodCliente.getText().trim().equals("")
                || !jFormattedTextFieldCodCliente.getText().matches("[0-9]*")) {
            JOptionPane.showMessageDialog(this, "Código do Cliente Inválido.",
                    "Cadastro de Locações", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldCodCliente.requestFocus();
            return false;
        }
        if (!jFormattedTextFieldDataLoc.getText().trim().equals("")
                && !LtpUtil.validarData(jFormattedTextFieldDataLoc.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Data de Locação Inválida.",
                    "Cadastro de Locações", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldDataLoc.requestFocus();
            return false;
        }
        return true;
    }

