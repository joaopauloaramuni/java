    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
        if (validarAtualizacao() == false) {
            return;
        }
        if (!jTextFieldCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Cliente já existe.",
                    "Cadastro de Clientes", JOptionPane.WARNING_MESSAGE);
            jTextFieldCodigo.requestFocus();
            return;
        }
        int codigo;
        try {
            codigo = (m_loc.buscarClienteUltimo().getCodigo()) + 1;
        } catch (Exception ex) {
            codigo = 1;
        }

        try {
            DateFormat formater = new SimpleDateFormat("dd/MM/yyyy");
            String as = jFormattedTextFieldData.getText();
            java.util.Date parsedUtilDate = formater.parse(as);
            java.sql.Date sqltDate = new java.sql.Date(parsedUtilDate.getTime());
            String nome = jFormattedTextFieldNome.getText();
            String CPF = jFormattedTextFieldCPF.getText();
            String Tel = jFormattedTextFieldTelefone.getText();
            String email = jFormattedTextFieldEmail.getText();

            m_loc.adicionarCliente(new Cliente(codigo, nome,
                    CPF, Tel,
                    email, sqltDate));
            JOptionPane.showMessageDialog(this, "Cliente Cadastrado.",
                    "Cadastro de Clientes", JOptionPane.INFORMATION_MESSAGE);
            limparForm();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage(),
                    "Cadastro de Clientes", JOptionPane.INFORMATION_MESSAGE);
        }

}//GEN-LAST:event_jButtonIncluirActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        if (jTextFieldCodigo.getText().equals("") || jTextFieldCodigo == null) {
            return;
        }
        if (JOptionPane.showConfirmDialog(this, "Confirmar a exclusão do cliente - "
                + jFormattedTextFieldNome.getText().trim() + "?",
                "Cadastro de Clientes",
                JOptionPane.YES_NO_OPTION) == JOptionPane.NO_OPTION) {
            return;
        }
        try {
            m_loc.excluirCliente(Integer.parseInt(jTextFieldCodigo.getText()));
            JOptionPane.showMessageDialog(this, "Cliente Excluído.",
                    "Cadastro de Clientes", JOptionPane.INFORMATION_MESSAGE);
            limparForm();

        } catch (SQLException objErro) {
            JOptionPane.showMessageDialog(this, objErro.getMessage(),
                    "Cadastro de Clientes", JOptionPane.WARNING_MESSAGE);

        }
}//GEN-LAST:event_jButtonExcluirActionPerformed

