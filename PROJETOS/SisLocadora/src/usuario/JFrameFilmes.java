    private boolean validarAtualizacao() {

        if (jFormattedTextFieldBrasil.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Falta informar o nome no brasil do filme.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldBrasil.requestFocus();
            return false;
        }
        if (jFormattedTextFieldOriginal.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Falta informar o nome original do filme.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldOriginal.requestFocus();
            return false;
        }
        if (jFormattedTextFieldAno.getText().trim().equals("") || !jFormattedTextFieldAno.getText().matches("[0-9]*")) {
            JOptionPane.showMessageDialog(this, "Ano Inválido.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldAno.requestFocus();
            return false;
        }
        if (jFormattedTextFieldGenero.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Falta informar o Gênero Principal do filme.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldGenero.requestFocus();
            return false;
        }
        if (jFormattedTextFieldAtor.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Falta informar o Ator Principal do filme.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldAtor.requestFocus();
            return false;
        }
        if (jFormattedTextFieldPontos.getText().trim().equals("") || !isFloat(jFormattedTextFieldPontos.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Pontuação IMDB Inválida.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldPontos.requestFocus();
            return false;
        }
        if (jFormattedTextFieldVotos.getText().trim().equals("") || !jFormattedTextFieldVotos.getText().matches("[0-9]*")) {
            JOptionPane.showMessageDialog(this, "Votos IMDB Inválidos.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldVotos.requestFocus();
            return false;
        }
        if (jFormattedTextFieldResumo.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Falta informar o Resumo IMDB do filme.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldResumo.requestFocus();
            return false;
        }
        if (jFormattedTextFieldTempo.getText().trim().equals("") || !jFormattedTextFieldTempo.getText().matches("[0-9]*")) {
            JOptionPane.showMessageDialog(this, "Tempo Inválido.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldTempo.requestFocus();
            return false;
        }
        if (jFormattedTextFieldEstoque.getText().trim().equals("") || !jFormattedTextFieldEstoque.getText().matches("[0-9]*")) {
            JOptionPane.showMessageDialog(this, "Estoque Inválido.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldEstoque.requestFocus();
            return false;
        }
        if (jFormattedTextFieldEmprestado.getText().trim().equals("") || !jFormattedTextFieldEmprestado.getText().matches("[0-9]*")) {
            JOptionPane.showMessageDialog(this, "Filmes emprestados inválidos.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldEmprestado.requestFocus();
            return false;
        }
        if (jFormattedTextFieldDiaria.getText().trim().equals("") || !isDouble(jFormattedTextFieldEmprestado.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Valor da diária inválido.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldDiaria.requestFocus();
            return false;
        }
        if (!jFormattedTextFieldData.getText().trim().equals("")
                && !LtpUtil.validarData(jFormattedTextFieldData.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Data Inválida.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jFormattedTextFieldData.requestFocus();
            return false;
        }
        return true;
    }

