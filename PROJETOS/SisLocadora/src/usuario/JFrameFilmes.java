    private void jButtonExecutarConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExecutarConsultaActionPerformed
        try {
            if (!LtpUtil.validarData(jTextFieldParametroData3.getText()) || !LtpUtil.validarData(jTextFieldParametroData4.getText())) {
                JOptionPane.showMessageDialog(
                        this,
                        "Data Inválida",
                        "Consulta de Filmes pelo Período",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }
            DateFormat formater = new SimpleDateFormat("dd/MM/yyyy");
            String as = jTextFieldParametroData3.getText();
            java.util.Date parsedUtilDate = formater.parse(as);
            java.sql.Date sqltDate = new java.sql.Date(parsedUtilDate.getTime());

            DateFormat formater2 = new SimpleDateFormat("dd/MM/yyyy");
            String as2 = jTextFieldParametroData4.getText();
            java.util.Date parsedUtilDate2 = formater2.parse(as2);
            java.sql.Date sqltDate2 = new java.sql.Date(parsedUtilDate2.getTime());

            jScrollPane11.setViewportView(m_loc.buscarFilmesPorPeriodo(sqltDate, sqltDate2));

            ResultSet rs = m_loc.buscarTotalLocacao(sqltDate, sqltDate2);
            jTextFieldParametroLoc.setText(rs.getString("TOTAL_LOCACOES"));
            jTextFieldParametroValue.setText(rs.getString("TOTAL_VALOR"));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage(),
                    "Consulta de Filmes pelo Período",
                    JOptionPane.WARNING_MESSAGE);
        }
}//GEN-LAST:event_jButtonExecutarConsultaActionPerformed

