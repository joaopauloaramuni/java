    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        if (validarAtualizacao() == false) {
            return;
        }
        if (jTextFieldCodigo.getText() == null || jTextFieldCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campo código vazio.",
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);
            jTextFieldCodigo.requestFocus();
            return;
        }
        try {
            DateFormat formater = new SimpleDateFormat("dd/MM/yyyy");
            String data = jFormattedTextFieldData.getText();
            java.util.Date parsedUtilDate = formater.parse(data);
            java.sql.Date sqltDate = new java.sql.Date(parsedUtilDate.getTime());

            int cod = Integer.parseInt(jTextFieldCodigo.getText());
            String nome = jFormattedTextFieldBrasil.getText();
            String nomeOriginal = jFormattedTextFieldOriginal.getText();
            int ano = Integer.parseInt(jFormattedTextFieldAno.getText());
            String genero = jFormattedTextFieldGenero.getText();
            String ator = jFormattedTextFieldAtor.getText();
            float pontos = Float.parseFloat(jFormattedTextFieldPontos.getText());
            int votos = Integer.parseInt(jFormattedTextFieldVotos.getText());
            String resumo = jFormattedTextFieldResumo.getText();
            int tempo = Integer.parseInt(jFormattedTextFieldTempo.getText());
            int estoque = Integer.parseInt(jFormattedTextFieldEstoque.getText());
            int emprestado = Integer.parseInt(jFormattedTextFieldEmprestado.getText());
            double valor = Double.parseDouble(jFormattedTextFieldDiaria.getText());

            m_loc.alterarFilme(new Filme(cod, nome,
                    nomeOriginal, ano,
                    genero, ator, pontos, votos, resumo, tempo, estoque, emprestado, valor, sqltDate));
            JOptionPane.showMessageDialog(this, "Filme Alterado.",
                    "Cadastro de Filmes", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro: " + ex.getMessage(),
                    "Cadastro de Filmes", JOptionPane.INFORMATION_MESSAGE);
        }
}//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        if (jTextFieldCodigo.getText().equals("") || jTextFieldCodigo == null) {
            return;
        }
        if (JOptionPane.showConfirmDialog(null, "Confirmar a exclusão do filme - "
                + jFormattedTextFieldBrasil.getText().trim() + "?",
                "Cadastro de Filme",
                JOptionPane.YES_NO_OPTION) == JOptionPane.NO_OPTION) {
            return;
        }
        try {
            m_loc.excluirFilme(Integer.parseInt(jTextFieldCodigo.getText()));
            JOptionPane.showMessageDialog(this, "Filme Excluído.",
                    "Cadastro de Filmes", JOptionPane.INFORMATION_MESSAGE);
            limparForm();

        } catch (SQLException objErro) {
            JOptionPane.showMessageDialog(this, objErro.getMessage(),
                    "Cadastro de Filmes", JOptionPane.WARNING_MESSAGE);

        }
}//GEN-LAST:event_jButtonExcluirActionPerformed

