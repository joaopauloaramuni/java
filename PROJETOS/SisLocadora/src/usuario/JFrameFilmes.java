        jFormattedTextFieldBrasil.setText(objFilme.getNomeBrasil().trim());
        jFormattedTextFieldOriginal.setText(objFilme.getNomeOriginal().trim());
        jFormattedTextFieldAno.setText(String.valueOf(objFilme.getAno()));
        jFormattedTextFieldGenero.setText(objFilme.getGenero().trim());
        jFormattedTextFieldAtor.setText(objFilme.getAtor().trim());
        jFormattedTextFieldPontos.setText(String.valueOf(objFilme.getPontos()));
        jFormattedTextFieldVotos.setText(String.valueOf(objFilme.getVotos()));
        jFormattedTextFieldResumo.setText(objFilme.getResumo().trim());
        jFormattedTextFieldTempo.setText(String.valueOf(objFilme.getTempo()));
        jFormattedTextFieldEstoque.setText(String.valueOf(objFilme.getEstoque()));
        jFormattedTextFieldEmprestado.setText(String.valueOf(objFilme.getEmprestado()));
        jFormattedTextFieldDiaria.setText(String.valueOf(objFilme.getValor()));
